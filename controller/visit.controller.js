const { all, paginate, create, findOne, update, destroy } = require('../generic/factoryControllers')
const Property = require('../schemas/property')
const Visit = require('../schemas/visit')

exports.GetAll = all(Visit, { include: { model: Property, attributes: ['street', 'number', 'floor', 'dept'] } })
exports.Paginate = paginate(Visit, { include: { model: Property, attributes: ['street', 'number', 'floor', 'dept'] } })
exports.Create = create(Visit)
exports.GetById = findOne(Visit)
exports.Put = update(Visit, ['PropertyId', 'date', 'fullName', 'phone', 'description', 'participants'])
exports.Destroy = destroy(Visit)
